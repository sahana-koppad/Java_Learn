package com.company.java.collection.set;

import java.util.HashSet;

public class HashSetDemo {

    public static void main(String[] args) {

        // Creating HashSet
        HashSet<String> set = new HashSet<>();

        // Adding elements
        set.add("Java");
        set.add("Python");
        set.add("C");

        // Printing HashSet
        System.out.println(set);
    }
}